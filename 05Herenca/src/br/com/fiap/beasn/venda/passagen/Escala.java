package br.com.fiap.beasn.venda.passagen;

public class Escala {
	private String local;
	private String tempo;
	private boolean trasnfer;
	
	public String getLoval() {
		return local;
	}
	public void setLoval(String loval) {
		this.local = loval;
	}
	public String getTempo() {
		return tempo;
	}
	public void setTempo(String tempo) {
		this.tempo = tempo;
	}
	public boolean isTrasnfer() {
		return trasnfer;
	}
	public void setTrasnfer(boolean trasnfer) {
		this.trasnfer = trasnfer;
	}
	public String getLocal() {
		return local;
	}
	public void setLocal(String local) {
		this.local = local;
	}
	
}
